# Tipos int e float
# int -> Número inteiro
# O tipo int representa qualquer número
# Positivo ou negativo. int sem sinal é considerado positivo

print(11) # int
print(-11) # int
print(0) # int

# float -> Número com ponto flutuante
# O tipo float representa qualquer número
# positivo ou negativo com ponto flutuante.
# float sem sinal é considerado positivo.

print(1.1, 10.11) # Na programação será sempre utilizado ponto ao invés de vírgula para separar casas decimais.
print(0.0, -1.5) # Se tem casa decimal é Float, se não tem casa Decimal é INT

# A função type mostra o tipo de Python inseriu ao valor.
print(type('Fabinho')) # Se possui letra minuscula e parentes para executar, É uma função!
print(type('123'))
print(type('-123'))
print(type(1.1), type(0.0), type(-1.1))